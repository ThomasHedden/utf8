void putu(unsigned int u) {
   fprintf(stdout, "%c%c%c%c", (char) ((u & 0xFF000000) >> 24),
                               (char) ((u & 0x00FF0000) >> 16),
                               (char) ((u & 0x0000FF00) >>  8),
                               (char) ((u & 0x000000FF)      ));
}

